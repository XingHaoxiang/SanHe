package com.zhuancheng.sanhedefence.domain.http.response;

/**
 * Created by cong on 2017/5/15.
 */
public class LoginResponse extends Response{

    private ResultBean result;

    public ResultBean getResult() {
        return result;
    }

    public void setResult(ResultBean result) {
        this.result = result;
    }

    public static class ResultBean {

        private int userType;
        private int userId;
        private String managerRealName;
        private String managerPhone;

        public int getUserType() {
            return userType;
        }

        public void setUserType(int userType) {
            this.userType = userType;
        }

        public int getUserId() {
            return userId;
        }

        public void setUserId(int userId) {
            this.userId = userId;
        }

        public String getManagerRealName() {
            return managerRealName;
        }

        public void setManagerRealName(String managerRealName) {
            this.managerRealName = managerRealName;
        }

        public String getManagerPhone() {
            return managerPhone;
        }

        public void setManagerPhone(String managerPhone) {
            this.managerPhone = managerPhone;
        }
    }
}
