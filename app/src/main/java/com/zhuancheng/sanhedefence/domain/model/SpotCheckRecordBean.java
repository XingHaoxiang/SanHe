public class SpotCheckRecordBean {
    private String id;
    private String proname;
    private String result;
    private String time;

    public SpotCheckRecordBean() {
    }

    public SpotCheckRecordBean(String id, String proname, String result, String time) {
        this.id = id;
        this.proname = proname;
        this.result = result;
        this.time = time;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProname() {
        return proname;
    }

    public void setProname(String proname) {
        this.proname = proname;
    }

    public String getResult() {
        return result;
    }

    public void setResult(String result) {
        this.result = result;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }
}
